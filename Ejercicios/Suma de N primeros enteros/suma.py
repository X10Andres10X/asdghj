# Programa No. 2: Calcular la suma de los N´primeros números enteros positivos,
# usando la formula  S = N ( N + 1 ) / 2


print("---------- Suma -----------")
print("-------- N primeros -------")
print("--------- números ---------")

# Input 
N = int(input("Digite el valor del número o números: "))

# Procesing
S = (N ( N + 1 )) / 2


# Output
print("---------- Resultados ----------")
print("La suma de los primeros N números es = " + str(S))
